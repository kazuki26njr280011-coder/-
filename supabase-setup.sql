-- -------------------------
-- Grants
-- RLSが実際の制御を行います。
-- -------------------------

grant usage on schema public to anon, authenticated;
grant select, insert, update, delete on all tables in schema public to authenticated;
grant usage, select on all sequences in schema public to authenticated;

-- -------------------------
-- Enable RLS
-- -------------------------

alter table public.profiles enable row level security;
alter table public.genres enable row level security;
alter table public.posts enable row level security;
alter table public.comments enable row level security;
alter table public.votes enable row level security;
alter table public.badges enable row level security;
alter table public.weekly_titles enable row level security;

-- -------------------------
-- Helper condition for admin
-- -------------------------
-- is_admin=true のprofiles行を持つユーザーを運営扱いにします。
-- 例:
-- update public.profiles set is_admin = true where username = 'admin_username';

-- -------------------------
-- Drop old policies
-- -------------------------

drop policy if exists "profiles are viewable by authenticated users" on public.profiles;
drop policy if exists "users can insert own profile" on public.profiles;

drop policy if exists "genres are viewable by authenticated users" on public.genres;
drop policy if exists "admins can insert genres" on public.genres;
drop policy if exists "admins can update genres" on public.genres;
drop policy if exists "admins can delete genres" on public.genres;

drop policy if exists "posts are viewable by authenticated users" on public.posts;
drop policy if exists "users can insert own posts" on public.posts;

drop policy if exists "comments are viewable by authenticated users" on public.comments;
drop policy if exists "users can insert own comments" on public.comments;
drop policy if exists "admins can delete comments" on public.comments;

drop policy if exists "votes are viewable by authenticated users" on public.votes;
drop policy if exists "users can insert own votes" on public.votes;

drop policy if exists "badges are viewable by authenticated users" on public.badges;
drop policy if exists "users can insert own or weekly title badges" on public.badges;

drop policy if exists "weekly titles are viewable by authenticated users" on public.weekly_titles;
drop policy if exists "authenticated users can insert weekly titles" on public.weekly_titles;
drop policy if exists "authenticated users can update weekly titles" on public.weekly_titles;
drop policy if exists "admins can delete weekly titles" on public.weekly_titles;

-- -------------------------
-- profiles policies
-- -------------------------

create policy "profiles are viewable by authenticated users"
on public.profiles
for select
to authenticated
using (true);

create policy "users can insert own profile"
on public.profiles
for insert
to authenticated
with check (auth.uid() = id and is_admin = false);

-- -------------------------
-- genres policies
-- -------------------------

create policy "genres are viewable by authenticated users"
on public.genres
for select
to authenticated
using (true);

create policy "admins can insert genres"
on public.genres
for insert
to authenticated
with check (
  exists (
    select 1 from public.profiles
    where profiles.id = auth.uid()
    and profiles.is_admin = true
  )
);

create policy "admins can update genres"
on public.genres
for update
to authenticated
using (
  exists (
    select 1 from public.profiles
    where profiles.id = auth.uid()
    and profiles.is_admin = true
  )
)
with check (
  exists (
    select 1 from public.profiles
    where profiles.id = auth.uid()
    and profiles.is_admin = true
  )
);

create policy "admins can delete genres"
on public.genres
for delete
to authenticated
using (
  exists (
    select 1 from public.profiles
    where profiles.id = auth.uid()
    and profiles.is_admin = true
  )
);

