create extension if not exists pgcrypto;

-- -------------------------
-- Tables
-- -------------------------

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text not null unique,
  display_name text not null,
  icon_url text,
  bio text default '',
  is_admin boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.genres (
  id bigint generated by default as identity primary key,
  name text not null unique,
  created_at timestamptz not null default now()
);

create table if not exists public.posts (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  genre text not null,
  image_url text not null,
  comment text not null check (char_length(comment) <= 2000),
  created_at timestamptz not null default now()
);

create table if not exists public.comments (
  id bigint generated by default as identity primary key,
  post_id bigint not null references public.posts(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  body text not null check (char_length(body) <= 1000),
  created_at timestamptz not null default now()
);

create table if not exists public.votes (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  post_id bigint not null references public.posts(id) on delete cascade,
  week_start date not null,
  created_at timestamptz not null default now(),
  constraint votes_one_per_user_per_week unique (user_id, week_start)
);

create table if not exists public.badges (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  badge_type text not null check (
    badge_type in (
      'first_post',
      'post_10',
      'kaikin_7',
      'kaikin_14',
      'comment_king',
      'reporter'
    )
  ),
  first_earned_at timestamptz not null default now(),
  constraint badges_unique_per_user unique (user_id, badge_type)
);

create table if not exists public.weekly_titles (
  id bigint generated by default as identity primary key,
  title_type text not null check (title_type in ('comment_king', 'reporter')),
  week_start date not null,
  user_id uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint weekly_titles_unique unique (title_type, week_start)
);

-- -------------------------
-- Indexes
-- -------------------------

create index if not exists posts_user_id_idx on public.posts(user_id);
create index if not exists posts_genre_idx on public.posts(genre);
create index if not exists posts_created_at_idx on public.posts(created_at desc);

create index if not exists comments_post_id_idx on public.comments(post_id);
create index if not exists comments_user_id_idx on public.comments(user_id);
create index if not exists comments_created_at_idx on public.comments(created_at desc);

create index if not exists votes_post_id_idx on public.votes(post_id);
create index if not exists votes_week_start_idx on public.votes(week_start);

create index if not exists badges_user_id_idx on public.badges(user_id);
create index if not exists weekly_titles_week_start_idx on public.weekly_titles(week_start desc);

