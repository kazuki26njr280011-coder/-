-- -------------------------
-- badges policies
-- 永久バッジは自分のみ。
-- comment_king / reporter はクライアント週次集計のためログインユーザーが記録可能。
-- -------------------------

create policy "badges are viewable by authenticated users"
on public.badges
for select
to authenticated
using (true);

create policy "users can insert own or weekly title badges"
on public.badges
for insert
to authenticated
with check (
  auth.uid() = user_id
  or badge_type in ('comment_king', 'reporter')
);

-- -------------------------
-- weekly_titles policies
-- 静的サイトで週次集計を更新するため、ログインユーザーにinsert/update許可。
-- -------------------------

create policy "weekly titles are viewable by authenticated users"
on public.weekly_titles
for select
to authenticated
using (true);

create policy "authenticated users can insert weekly titles"
on public.weekly_titles
for insert
to authenticated
with check (true);

create policy "authenticated users can update weekly titles"
on public.weekly_titles
for update
to authenticated
using (true)
with check (true);

create policy "admins can delete weekly titles"
on public.weekly_titles
for delete
to authenticated
using (
  exists (
    select 1 from public.profiles
    where profiles.id = auth.uid()
    and profiles.is_admin = true
  )
);

-- -------------------------
-- Storage policies
-- -------------------------

drop policy if exists "post images are publicly readable" on storage.objects;
drop policy if exists "authenticated users can upload own post images" on storage.objects;
drop policy if exists "users can update own post images" on storage.objects;
drop policy if exists "users can delete own post images" on storage.objects;

create policy "post images are publicly readable"
on storage.objects
for select
to public
using (bucket_id = 'post-images');

create policy "authenticated users can upload own post images"
on storage.objects
for insert
to authenticated
with check (
  bucket_id = 'post-images'
  and (storage.foldername(name))[1] = auth.uid()::text
);

create policy "users can update own post images"
on storage.objects
for update
to authenticated
using (
  bucket_id = 'post-images'
  and (storage.foldername(name))[1] = auth.uid()::text
)
with check (
  bucket_id = 'post-images'
  and (storage.foldername(name))[1] = auth.uid()::text
);

create policy "users can delete own post images"
on storage.objects
for delete
to authenticated
using (
  bucket_id = 'post-images'
  and (storage.foldername(name))[1] = auth.uid()::text
);
